function player () {
    fill (107, 56, 194);
    ellipse(x,y,50,50);  
    noStroke ();
    fill(255, 128, 0);
    triangle (x-16.5,y-18.6,x,y,x+16.5,y-18.6);
    triangle (x+23.8,y+6.2,x,y,x+23.8,y-6.2);
    triangle (x-16.5,y+18.6,x,y,x+16.5,y+18.6);
    triangle (x-23.8,y+6.2,x,y,x-23.8,y-6.2);
    
    x = x + speedX;
    y = y + speedY;
    
    if (y <= 75) {
        speedY = 0;
}
    if (y >= 325) {
        speedY = 0;
}
    if ( mouseIsPressed) {
        if (sttus === 1) {
            speedY = 4;
            sttus = 2;
        } else {
            speedY = -4;
            sttus = 1;
        }
    }
};    

